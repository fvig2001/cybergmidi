void convertSNtoENSingle(std::vector<SequencerNote> source, std::vector<EncodedNote> &converted)
{
    int curOffset = 1;
    int curOrder = 0;
    converted.clear();
    for (int i = 0; i < source.size(); i++)
    {
        EncodedNote destination;
        destination.midiNote = source[i].note;
        destination.velocity = source[i].velocity;
        destination.relativeOctave = source[i].relativeOctave;
        destination.length = source[i].holdTime;
        destination.lengthTicks = source[i].holdTime;
        destination.channel = source[i].channel;
        if (source[i].offset != curOffset)
        {
            curOrder++;
            curOffset = source[i].offset;
        }
        destination.noteOrder = curOrder;
        converted.push_back(destination);
    }
}

void convertSNtoEN()
{
    convertSNtoENSingle(DrumLoopSequencer, DrumLoopSequencerE);
    convertSNtoENSingle(AccompanimentSequencerPattern, AccompanimentSequencerPatternE);
    convertSNtoENSingle(BassSequencerPattern, BassSequencerPatternE);
}
void getOmniChordBacking(int newBackingType, std::vector<EncodedNote>& drumOut, std::vector<EncodedNote>& bassOut, std::vector<EncodedNote>& accompanimentOut)
{
    //clear data they overwrite
    DrumLoopSequencer.clear();
    BassSequencerPattern.clear();
    AccompanimentSequencerPattern.clear();

    switch (newBackingType)
    {
    case 1:
        generateRock1();
        break;
    case 2:
        generateRock2();
        break;
    case 3:
        generateBossanova();
        break;
    case 4:
        generateFunk();
        break;
    case 5:
        generateCountry();
        break;
    case 6:
        generateDisco();
        break;
    case 7:
        generateSlowRock();
        break;
    case 8:
        generateSwing();
        break;
    case 9:
        generateWaltz();
        break;
    default:
        generateHiphop();
    }
    //convert sequencer Note to encoded Note
    convertSNtoEN();
    drumOut = DrumLoopSequencerE;
    bassOut = BassSequencerPatternE;
    accompanimentOut = AccompanimentSequencerPatternE;
}


